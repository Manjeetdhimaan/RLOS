IF OBJECT_ID ('dbo.NG_RLOS_PORTAL_LOAD_PREFERENCE') IS NOT NULL
	DROP PROCEDURE dbo.NG_RLOS_PORTAL_LOAD_PREFERENCE
GO

CREATE PROCEDURE [dbo].[NG_RLOS_PORTAL_LOAD_PREFERENCE]
(
	@ARN NVARCHAR(100)
)
AS
BEGIN
	SELECT LAST_VISITED_PAGE, VISITOR_IP, STATUS FROM NG_RLOS_PORTAL_PREFRENCES WHERE Workitem_Name = @ARN
END

GO